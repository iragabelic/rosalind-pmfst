def AddImaginaryEdge(D):
  ulazni = Counter()
  izlazni = Counter()

  for key, value in D.items():
    izlazni[key] += len(value)
    for v in value: 
      ulazni[v] += 1
  
  start = list(ulazni - izlazni)[0]
  end = list(izlazni - ulazni)[0]

  D[start] = [end]
  return start, end


